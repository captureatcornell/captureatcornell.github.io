$(document).ready(function () {
  $('.card-back').addClass('hidden');
});

$('#demers').click(function () {
  $('#demers').toggleClass('flip-card');
  setTimeout(function () {
    $('#demers .card-front').toggleClass('hidden');
    $('#demers .card-back').toggleClass('hidden');
  }, 300);
});

